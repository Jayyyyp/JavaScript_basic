// 거듭제곱 연산
var num = 2;
console.log(num ** 5); // 2의 5승
console.log(num ** 1/2); // 루트 2

// 리터럴 문자열을 활용해 아래와 같은 문장 출력하기
// 2의 20승은 ~~~~~ 입니다.
var result = num ** 20;
console.log(`${num}의 20승은 ${result} 입니다.`);

